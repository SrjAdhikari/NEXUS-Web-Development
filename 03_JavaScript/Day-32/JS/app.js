const filter_btn = document.querySelector(".filter");
const alcohol_btn = document.querySelector(".alcohol");
const offers_btn = document.querySelector(".offers");
const rating_btn = document.querySelector(".rate");
const apply_btn = document.querySelector("#applyFilter");
const close_btn = document.querySelector("#closeFilter");
const popup = document.getElementById("filterPopup");


alcohol_btn.addEventListener("click", () => {
    const filtered_restaurants = restaurant_info.filter(restaurant => {
        return restaurant.serve_alcholol;
    });
    root_container.innerHTML = "";
    displayRestaurants(filtered_restaurants);
});

offers_btn.addEventListener("click", () => {
    const filtered_restaurants = restaurant_info.filter(restaurant => {
        return restaurant.offers > 0;
    });
    root_container.innerHTML = "";
    displayRestaurants(filtered_restaurants);
});

rating_btn.addEventListener("click", () => {
    const filtered_restaurants = restaurant_info.filter(restaurant => {
        return restaurant.rating > 4.5;
    });
    root_container.innerHTML = "";
    displayRestaurants(filtered_restaurants);
});

filter_btn.addEventListener("click", () => {
    popup.classList.toggle("hidden");
});

apply_btn.addEventListener("click", () => {
    const selectedOption = document.querySelector("input[name='filterOption']:checked");
    const selectedValue = selectedOption ? selectedOption.value : null;
    if(selectedValue === "rating") {
        restaurant_info.sort((a, b) => b.rating - a.rating);
    }
    else if(selectedValue === "highLow") {
        restaurant_info.sort((a, b) => b.price_for_two - a.price_for_two);
    }
    else if(selectedValue === "costLowHigh") {
        restaurant_info.sort((a, b) => a.price_for_two - b.price_for_two);
    }
    else if(selectedValue === "distance") {
        restaurant_info.sort((a, b) => a.distance - b.distance);
    }

    root_container.innerHTML = "";
    displayRestaurants(restaurant_info);
    popup.classList.toggle("hidden");
});

close_btn.addEventListener("click", () => {
    popup.classList.toggle("hidden");
});