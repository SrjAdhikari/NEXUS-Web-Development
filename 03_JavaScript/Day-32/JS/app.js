const root_container = document.getElementById("root");
const filter_container = document.querySelector(".filter-card");

const displayRestaurants = (restaurants) => {
    restaurants.forEach((restaurant) => {
        // Create a card element for each restaurant
        const card_element = document.createElement("div");
        card_element.classList.add("card");

        // Create an image element for the restaurant
        const image = document.createElement("img");
        image.src = `./images/${restaurant.image}.jpeg`;

        // Create a container for the card content
        const card_content = document.createElement("div");
        card_content.classList.add("card-content");

        // Create a heading element for the restaurant name
        const card_header = document.createElement("div");
        card_header.classList.add("card-header");

        const h3 = document.createElement("h3");
        h3.textContent = restaurant.name;

        const rating = document.createElement("span")
        rating.textContent = `Rating: ${restaurant.rating}`;
        rating.classList.add("rating");

        card_header.append(h3, rating);

        const card_footer = document.createElement("div");
        card_footer.classList.add("card-footer");

        const food_type = document.createElement("span");
        food_type.textContent = restaurant.food_type;

        const price = document.createElement("span");
        price.textContent = `₹${restaurant.price_for_two}`;

        card_footer.append(food_type, price);

        const card_location = document.createElement("div");
        card_location.classList.add("card-location");

        const location = document.createElement("span");
        location.textContent = restaurant.location;

        const distance = document.createElement("span");
        distance.textContent = `${restaurant.distance} km`;

        card_location.append(location, distance);
        card_content.append(card_header, card_footer, card_location);
        card_element.append(image, card_content);

        root_container.appendChild(card_element);

    });
};

