//* What is TypeScript ?
    // TypeScript is a strongly typed programming language that can detect errors in the code during the compile time.
    // It is a superset of JavaScript that adds static typing, which helps identify errors during development.


//* ****************************************************
//* Why Use TypeScript ?
//* ****************************************************

    // 1. Static typing: 
        // TypeScript provides static typing, which means that the type of a variable is known at compile time.

    // 2. Strong typing: 
        // TypeScript enforces strong typing, which means that the type of a variable must be explicitly declared.

    // 3. Code organization: 
        // TypeScript provides code organization features, such as namespaces, modules, and interfaces.

    // 4. Error detection:
        // TypeScript can detect errors in the code during the compile time, which helps to catch errors early in the development process.


//* ****************************************************
//* How To Use TypeScript ?
//* ****************************************************

    // 1. Install TypeScript: 
        // Run "npm install -g typescript" to install TypeScript globally.

    // 2. Initialize a TypeScript project: 
        // Run "tsc --init" to initialize a TypeScript project.

    // 3. Create a TypeScript file: 
        // Create a new file with a ".ts" extension and add your TypeScript code.

    // 4. Compile TypeScript to JavaScript: 
        // Run "tsc filename.ts" to compile your TypeScript file to JavaScript.

    // 5. Run the compiled JavaScript file: 
        // Run "node filename.js" to run the compiled JavaScript file.


//* ****************************************************
//* TypeScript Features
//* ****************************************************

    // 1. TypeScript adds types to JavaScript, making it more predictable and safer.
    // 2. TypeScript helps explicitly define types for variables, functions, and objects.
    // 3. TypeScript helps with catching errors early in the development process.
    // 4. TypeScript compiles down to JavaScript, so it can run in any JavaScript environment.
    // 5. TypeScript is a superset of JavaScript, which means it has all the features of JavaScript plus additional features.